#include "libftprintf.h"

int	ft_putnbr(long n)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		n *= -1;
		i += ft_putchar('-');
	}
	if (n / 10)
		ft_putnbr (n / 10);
	i += ft_putchar (n % 10 + '0');
	return i;
}
