#include "libftprintf.h"

void	ft_putnbr_fd(long n, int fd)
{

	if (n < 0)
	{
		n *= -1;
		ft_putchar_fd('-', fd);
	}
	if (n / 10)
		ft_putnbr_fd (n / 10, fd);
	ft_putchar_fd (n % 10 + '0', fd);
}
