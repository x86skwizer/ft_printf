#include "libftprintf.h"

int	ft_putstr(char *s)
{
	int	i;
	int n;

	i = 0;
	n = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			n += ft_putchar(s[i]);
			i++;
		}
	}
	return (n);
}
